// Формула выборки: 2 уникальных отзыва для каждой из 10 карточек, без повторений
export const getReviewsForWork = (reviews: Review[], workId: number): Review[] => {
  const offset = (workId - 1) * 2;
  return [
    reviews[offset % reviews.length],
    reviews[(offset + 1) % reviews.length],
  ];
};
